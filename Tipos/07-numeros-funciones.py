import math #Importamos el módulo para acceder a mas funciones para trabajar con números

print(round(1.3)) #Nos muestra el número al cual está más cerca el decimal (1)
print(round(1.7)) #Nos muestra el número al cual está más cerca el decimal (2)
print(abs(-77)) #Nos muestra el valor absoluto

print(math.ceil(1.1)) #Muestra el número entero superior más cercano del número ingresado
print(math.floor(1.9)) #Muestra el número entero inferior más cercano del número ingresado
print(math.isnan(10)) #Me dice si el valor ingresado NO es un número mediante un boolean
print(math.pow(10, 3)) #Me muestra el resultado de una potencia (10 elevado a la 3)
print(math.sqrt(81)) #Me muestra la raiz cuadrada del número ingresado
